                            daycount++;
                        }
                    }

                    verseCount += VersesInChapter[i][j];

                    versePerDayNtPvPs[daycount].Add(chapterCount);

                    while ((verseCount) > (goalVerseNumNtPvPs*(daycount + 1)) && versePerDayNtPvPs[daycount].Count() > 0)
                    {
                        daycount++;
                    }
                    chapterCount++;
                }
            }

